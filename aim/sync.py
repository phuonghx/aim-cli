def _p(root_dir, rel_path):
    """Return an absolute path when root_dir is known, else the original relative path.
    Uses forward slashes for cross-platform agent readability."""
    if root_dir:
        return os.path.join(root_dir, rel_path).replace("\\", "/")
    return rel_path


