# Files that aim sync generates, grouped for gitignore management.
_GITIGNORE_RULES = [
    "CLAUDE.md", "ANTIGRAVITY.md", "GEMINI.md", "AGENTS.md",
    ".cursorrules", ".windsurfrules",
    ".github/copilot-instructions.md",
]
_GITIGNORE_COMMANDS = [
    ".claude/commands/aim-*",
    ".cursor/rules/aim-*",
    ".windsurf/rules/aim-*",
]

_GITIGNORE_MARKER_BEGIN = "# AIM generated (managed by `aim sync` — do not edit this block)"
_GITIGNORE_MARKER_END = "# /AIM"


def manage_gitignore(root_dir, strategy="track-all", enabled_agents=None):
    """Add or remove AIM entries from .gitignore based on strategy.

    Strategies:
    - track-all:  remove AIM entries from .gitignore (commit everything)
    - ignore-all: add all AIM-generated files to .gitignore
    - rules-only: track rule files, ignore slash commands
    """
    gi_path = os.path.join(root_dir, ".gitignore")

    # Build the set of entries to ignore
    entries = []
    if strategy == "ignore-all":
        entries = _GITIGNORE_RULES + _GITIGNORE_COMMANDS
    elif strategy == "rules-only":
        entries = _GITIGNORE_COMMANDS  # only ignore slash commands

    # Read existing .gitignore
    existing = ""
    if os.path.exists(gi_path):
        with open(gi_path, "r", encoding="utf-8") as f:
            existing = f.read()

    # Remove old AIM block if present
    if _GITIGNORE_MARKER_BEGIN in existing:
        before = existing.split(_GITIGNORE_MARKER_BEGIN, 1)[0]
        after_parts = existing.split(_GITIGNORE_MARKER_END, 1)
        after = after_parts[1] if len(after_parts) > 1 else ""
        existing = before.rstrip("\n") + after.lstrip("\n")

    # Add new block if there are entries
    if entries:
        block = (f"\n{_GITIGNORE_MARKER_BEGIN}\n"
                 + "\n".join(entries)
                 + f"\n{_GITIGNORE_MARKER_END}\n")
        new_content = existing.rstrip("\n") + "\n" + block
    else:
        new_content = existing

    with open(gi_path, "w", encoding="utf-8") as f:
        f.write(new_content)


def main():
    root_dir = find_project_root()
    print(f"[*] Detected project root: {root_dir}")

    config = load_config(root_dir)
    print("[*] Loaded configuration file successfully.")

    enabled_agents = config.get("agents", {})
    imported = imported_rules_section(root_dir)

    for label, rel_path, generator in SYNC_TARGETS:
        agent_key = TARGET_AGENT_MAP.get(rel_path)
        if agent_key and not enabled_agents.get(agent_key, True):
            print(f"[ ] Skipped {label} (agent '{agent_key}' disabled)")
            continue
        target_path = os.path.join(root_dir, rel_path)
        parent_dir = os.path.dirname(target_path)
        if parent_dir and not os.path.exists(parent_dir):
            os.makedirs(parent_dir)
        write_managed_file(target_path, generator(config, root_dir) + imported,
                           header_if_new=NEW_FILE_HEADERS.get(rel_path))
        print(f"[+] Synchronized {target_path}")

    if config.get("skillCommands", True):
        counts = generate_skill_commands(root_dir, enabled_agents)
        if counts:
            total = sum(counts.values())
            parts = [f"{k.title()}: {v}" for k, v in counts.items() if v > 0]
            print(f"[+] Generated {total} slash commands ({', '.join(parts)})")
    else:
        print("[ ] Skill commands disabled in config")

    # Manage .gitignore
    git_strategy = config.get("gitTracking", "track-all")
    if git_strategy != "track-all":
        manage_gitignore(root_dir, git_strategy, enabled_agents)
        print(f"[+] Updated .gitignore (strategy: {git_strategy})")

    print("[+] Synchronization completed successfully!")

if __name__ == "__main__":
    main()

