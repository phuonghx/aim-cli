# Markers delimiting the AIM-managed region inside generated rule files.
# Content OUTSIDE the markers is user-owned and always preserved.
AIM_BEGIN = "<!-- AIM:BEGIN (auto-generated by `aim sync` - edits inside this block will be overwritten) -->"
AIM_END = "<!-- AIM:END -->"

def find_project_root():
    # Walk up from the current directory looking for an initialized workspace.
    cwd = os.getcwd()
    current = cwd
    while True:
        if os.path.exists(os.path.join(current, ".ai-context", "config.json")):
            return current
        parent = os.path.dirname(current)
        if parent == current:
            break
        current = parent
    # Fallback to the directory of this script's parent if it exists
    script_dir = os.path.dirname(os.path.abspath(__file__))
    script_parent = os.path.dirname(script_dir)
    if os.path.exists(os.path.join(script_parent, ".ai-context", "config.json")):
        return script_parent
    return cwd

def load_config(root_dir):
    config_path = os.path.join(root_dir, ".ai-context", "config.json")
    if not os.path.exists(config_path):
        print(f"[-] Configuration file not found at {config_path}")
        print("[-] Please run 'aim init' first to initialize the workspace.")
        sys.exit(1)

    with open(config_path, "r", encoding="utf-8") as f:
        return json.load(f)

def write_managed_file(path, generated_content, header_if_new=None):
    """Write generated content into the AIM-managed block of `path`.

    - New file: the whole file is the managed block (optionally preceded by
      `header_if_new`, e.g. MDC frontmatter that must sit at the very top).
    - Existing file with markers: only the region between the markers is
      replaced; everything the user wrote around it is preserved.
    - Existing file without markers (pre-AIM or hand-written): the original
      is backed up to <name>.bak once, then the managed block is appended
      below the user's content - nothing is deleted.
    """
    block = f"{AIM_BEGIN}\n{generated_content.strip()}\n{AIM_END}\n"

    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            existing = f.read()
        if AIM_BEGIN in existing and AIM_END in existing:
            prefix = existing.split(AIM_BEGIN, 1)[0]
            suffix = existing.split(AIM_END, 1)[1]
            new_content = prefix + block + suffix
        else:
            backup_path = path + ".bak"
            if not os.path.exists(backup_path):
                with open(backup_path, "w", encoding="utf-8") as f:
                    f.write(existing)
                print(f"[*] Existing {os.path.basename(path)} had no AIM markers; backed it up to {os.path.basename(backup_path)}.")
            new_content = existing.rstrip("\n") + "\n\n" + block
    else:
        new_content = (header_if_new or "") + block

    tmp_path = f"{path}.{os.getpid()}.tmp"
    with open(tmp_path, "w", encoding="utf-8") as f:
        f.write(new_content)
    os.replace(tmp_path, path)

def _suite_counts():
    """Count the bundled specialist agents, skills, and workflows so the
    generated instruction files never quote a stale number. Reads the packaged
    templates relative to this module; returns (agents, skills, workflows)."""
    base = os.path.join(os.path.dirname(os.path.abspath(__file__)), "templates", "aim-agents")

    def _n(sub, ext=None):
        d = os.path.join(base, sub)
        try:
            entries = os.listdir(d)
        except OSError:
            return 0
        if ext is None:  # count sub-directories (skills are folders)
            return sum(1 for e in entries if os.path.isdir(os.path.join(d, e)))
        return sum(1 for e in entries if e.endswith(ext))

    return _n("agent", ".md"), _n("skills"), _n("workflows", ".md")


