def imported_rules_section(root_dir):
    """Concatenate rules collected by `aim ingest` (.ai-context/imported/*.md)
    into one markdown section, re-emitted into every client file so scattered
    rules become a single consolidated source of truth. Returns '' if none."""
    imported_dir = os.path.join(root_dir, ".ai-context", "imported")
    if not os.path.isdir(imported_dir):
        return ""
    blocks = []
    for name in sorted(os.listdir(imported_dir)):
        if name.endswith(".md"):
            try:
                with open(os.path.join(imported_dir, name), "r", encoding="utf-8") as f:
                    body = f.read().strip()
            except OSError:
                continue
            if body:
                blocks.append(body)
    if not blocks:
        return ""
    return "\n\n## Imported Project Rules\n\n" + "\n\n".join(blocks) + "\n"

def _parse_skill_frontmatter(path):
    """Parse YAML-like frontmatter from a SKILL.md or agent .md file.
    Returns dict with 'name' and 'description' (or empty dict on failure)."""
    try:
        with open(path, "r", encoding="utf-8") as f:
            content = f.read()
    except OSError:
        return {}
    if not content.startswith("---"):
        return {}
    parts = content.split("---", 2)
    if len(parts) < 3:
        return {}
    fm = {}
    for line in parts[1].strip().splitlines():
        if ":" in line:
            key, _, val = line.partition(":")
            fm[key.strip()] = val.strip()
    return fm


def _collect_skills(root_dir):
    """Collect all skills, agent personas, and workflows from project.
    Returns list of dicts: {type, name, description, rel_path}."""
    items = []

    # 1. Domain skills (.aim-agents/skills/<name>/SKILL.md)
    skills_dir = os.path.join(root_dir, ".aim-agents", "skills")
    if os.path.isdir(skills_dir):
        for name in sorted(os.listdir(skills_dir)):
            skill_file = os.path.join(skills_dir, name, "SKILL.md")
            if os.path.isfile(skill_file):
                fm = _parse_skill_frontmatter(skill_file)
                desc = fm.get("description", f"AIM skill: {name}")
                items.append({
                    "type": "skill",
                    "name": name,
                    "command_name": f"aim-{name}",
                    "description": desc,
                    "rel_path": f".aim-agents/skills/{name}/SKILL.md",
                })

    # 2. Core skills (.ai-context/skills/<name>/SKILL.md)
    core_dir = os.path.join(root_dir, ".ai-context", "skills")
    if os.path.isdir(core_dir):
        for name in sorted(os.listdir(core_dir)):
            skill_file = os.path.join(core_dir, name, "SKILL.md")
            if os.path.isfile(skill_file):
                fm = _parse_skill_frontmatter(skill_file)
                desc = fm.get("description", f"AIM core skill: {name}")
                items.append({
                    "type": "skill",
                    "name": name,
                    "command_name": f"aim-{name}",
                    "description": desc,
                    "rel_path": f".ai-context/skills/{name}/SKILL.md",
                })

    # 3. Agent personas (.aim-agents/agent/<name>.md)
    agent_dir = os.path.join(root_dir, ".aim-agents", "agent")
    if os.path.isdir(agent_dir):
        for fname in sorted(os.listdir(agent_dir)):
            if fname.endswith(".md"):
                agent_name = fname[:-3]
                agent_file = os.path.join(agent_dir, fname)
                fm = _parse_skill_frontmatter(agent_file)
                desc = fm.get("description", f"AIM agent persona: {agent_name}")
                items.append({
                    "type": "agent",
                    "name": agent_name,
                    "command_name": f"aim-agent-{agent_name}",
                    "description": desc,
                    "rel_path": f".aim-agents/agent/{fname}",
                })

    # 4. Workflows (.aim-agents/workflows/<name>.md)
    wf_dir = os.path.join(root_dir, ".aim-agents", "workflows")
    if os.path.isdir(wf_dir):
        for fname in sorted(os.listdir(wf_dir)):
            if fname.endswith(".md"):
                wf_name = fname[:-3]
                wf_file = os.path.join(wf_dir, fname)
                fm = _parse_skill_frontmatter(wf_file)
                desc = fm.get("description", f"AIM workflow: {wf_name}")
                items.append({
                    "type": "workflow",
                    "name": wf_name,
                    "command_name": f"aim-workflow-{wf_name}",
                    "description": desc,
                    "rel_path": f".aim-agents/workflows/{fname}",
                })

    return items


def _truncate(text, max_len=200):
    """Truncate text to max_len characters for frontmatter descriptions."""
    if len(text) <= max_len:
        return text
    return text[:max_len - 3] + "..."


def generate_skill_commands(root_dir, enabled_agents=None):
    """Generate /aim-<skill> slash command files for all supported platforms.

    If enabled_agents is provided, only generate for enabled platforms.

    Platforms:
    - Claude Code:   .claude/commands/aim-*.md
    - Cursor:        .cursor/rules/aim-*.mdc
    - Windsurf:      .windsurf/rules/aim-*.md
    - Antigravity:   ~/.gemini/config/plugins/aim-skills/skills/aim-*.md
    """
    items = _collect_skills(root_dir)
    if not items:
        return 0

    abs_root = root_dir.replace("\\", "/")
    counts = {"claude": 0, "cursor": 0, "windsurf": 0, "antigravity": 0}
    if enabled_agents is None:
        enabled_agents = {"claude": True, "cursor": True,
                          "windsurf": True, "antigravity": True}

    def _write_all(out_dir, ext, make_content, key):
        os.makedirs(out_dir, exist_ok=True)
        for item in items:
            path = os.path.join(out_dir, f"{item['command_name']}{ext}")
            with open(path, "w", encoding="utf-8") as fh:
                fh.write(make_content(item))
            counts[key] += 1

    # ---- Claude Code ----
    if enabled_agents.get("claude", True):
        def _claude(it):
            return (f'---\ndescription: "{_truncate(it["description"])}"\n---\n\n'
                    f'Read and strictly follow the instructions from the file:\n'
                    f'`{abs_root}/{it["rel_path"]}`\n\n'
                    f'If the skill references additional sub-files '
                    f'(e.g. in a file index table), read those relevant files too.\n\n'
                    f'Apply the skill\'s guidelines to complete this task:\n\n'
                    f'$ARGUMENTS\n')
        _write_all(os.path.join(root_dir, ".claude", "commands"),
                   ".md", _claude, "claude")

    # ---- Cursor ----
    if enabled_agents.get("cursor", True):
        def _cursor(it):
            return (f'---\ndescription: "{_truncate(it["description"])}"\n'
                    f'alwaysApply: false\n---\n\n'
                    f'Read and strictly follow the instructions from the file:\n'
                    f'`{abs_root}/{it["rel_path"]}`\n\n'
                    f'If the skill references additional sub-files '
                    f'(e.g. in a file index table), read those relevant files too.\n\n'
                    f'Apply the skill\'s guidelines to the current task.\n')
        _write_all(os.path.join(root_dir, ".cursor", "rules"),
                   ".mdc", _cursor, "cursor")

    # ---- Windsurf ----
    if enabled_agents.get("windsurf", True):
        def _windsurf(it):
            return (f'---\ntrigger: manual\n'
                    f'description: "{_truncate(it["description"])}"\n---\n\n'
                    f'Read and strictly follow the instructions from the file:\n'
                    f'`{abs_root}/{it["rel_path"]}`\n\n'
                    f'If the skill references additional sub-files '
                    f'(e.g. in a file index table), read those relevant files too.\n\n'
                    f'Apply the skill\'s guidelines to the current task.\n')
        _write_all(os.path.join(root_dir, ".windsurf", "rules"),
                   ".md", _windsurf, "windsurf")

    # ---- Antigravity ----
    if enabled_agents.get("antigravity", True):
        home = os.path.expanduser("~")
        ag_dir = os.path.join(home, ".gemini", "config", "plugins", "aim-skills")
        sk_dir = os.path.join(ag_dir, "skills")
        os.makedirs(sk_dir, exist_ok=True)
        
        # Clean up legacy .md files in sk_dir
        if os.path.exists(sk_dir):
            for f in os.listdir(sk_dir):
                f_path = os.path.join(sk_dir, f)
